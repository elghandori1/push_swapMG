#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		s;
	char	*dup;

	s = ft_strlen(s1);
	dup = (char *)malloc(s * sizeof(char) + 1);
	if (dup == NULL)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
